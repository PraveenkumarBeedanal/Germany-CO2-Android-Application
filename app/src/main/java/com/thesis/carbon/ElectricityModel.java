package com.thesis.carbon;

public class ElectricityModel {

    private int resource_image;
    private int resource_val;
    private String resource_name;

    public int getResource_image() {
        return resource_image;
    }

    public void setResource_image(int resource_image) {
        this.resource_image = resource_image;
    }

    public int getResource_val() {
        return resource_val;
    }

    public void setResource_val(int resource_val) {
        this.resource_val = resource_val;
    }

    public String getResource_name() {
        return resource_name;
    }

    public void setResource_name(String resource_name) {
        this.resource_name = resource_name;
    }
}
